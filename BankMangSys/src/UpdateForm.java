    private void jTextField2KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyTyped
        char c = evt.getKeyChar();
        if (!(jTextField2.getText().length() < 11)) {
            evt.consume();
        }
        if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || (c == KeyEvent.VK_DELETE))) {
            evt.consume();
        }
    }//GEN-LAST:event_jTextField2KeyTyped

    public void update() {
        String acctype;
        if (jRadioButton1.isSelected()) {
            acctype = "Savings";
        }
        if (jRadioButton2.isSelected()) {
            acctype = "Current";
        } else {
            acctype = "Term Deposit";
        }
        try {
            m.connect();
            DisplayAccount da = new DisplayAccount();
            System.out.println("uf accNo  =" + accNo);
            String address = "UPDATE AccountsInfo SET Address = '" + jTextField1.getText() + "' where AccountNo=" + accNo;
            String contact = "UPDATE AccountsInfo SET ContactNo = '" + jTextField2.getText() + "' where AccountNo=" + accNo;
            String accTypeSql = "UPDATE AccountsInfo SET AccountType = '" + acctype + "' where AccountNo=" + accNo;
            m.stmt = m.conn.createStatement();
            m.stmt.executeUpdate(address);
            m.stmt.executeUpdate(contact);
            m.stmt.executeUpdate(accTypeSql);
            JOptionPane.showMessageDialog(rootPane, "Record has been updated");
            this.setVisible(false);
            da.checkAndDisplayAcc(accNo);
            m.closeConn();
        } catch (Exception e) {
            System.out.println("Update Form Exception: " + e);
        }
    }

