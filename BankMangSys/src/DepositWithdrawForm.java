    private void ProceedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ProceedActionPerformed
        int a = JOptionPane.showConfirmDialog(null, " Are You Sure ?");
        if (a == 0) {
            if (Name.getText().equals("") || AccountNo.getText().equals("") || Amount.getText().equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Fill all fields");
            } else {
                if (jComboBox1.getSelectedItem() == "Deposit") {
                    DepositWithdraw dw = new DepositWithdraw(AccountNo.getText(), Name.getText());
                    dw.deposit(Double.parseDouble(Amount.getText()));
                } else {
                    DepositWithdraw dw = new DepositWithdraw(AccountNo.getText(), Name.getText());
                    dw.withdraw(Double.parseDouble(Amount.getText()));
                }
                this.setVisible(false);
            }
        } else {
        }
    }//GEN-LAST:event_ProceedActionPerformed

